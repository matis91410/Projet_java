    private void ajoutergestionnaireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajoutergestionnaireActionPerformed
        // TODO add your handling code here:
        
        try{
                
            Connection con = getconnexion();
            
            String nom = edtnom.getText();
            String prenom = edtprenom.getText();
            String email = edtmail.getText();
            String login = edtlogin.getText();
            String mdp = edtpassword.getText();
            
            String sql = "INSERT INTO clients(nom,prenom,email,login,mdp,id_types_fonction) VALUES (?, ?, ?, ?,?,2)";
            
            PreparedStatement ps = con.prepareStatement(sql);
            
            ps.setString(1,nom);
            ps.setString(2,prenom);
            ps.setString(3,email);
            ps.setString(4,login);
            ps.setString(5,mdp);
            ps.executeUpdate();
            
         
            JOptionPane.showMessageDialog(this, "Gestionnaire Ajouter avec succès !");
            
            con.close();
            ps.close();
            
        } 
        catch (SQLException e){
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_ajoutergestionnaireActionPerformed

