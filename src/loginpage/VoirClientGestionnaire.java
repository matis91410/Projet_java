    public ArrayList<User> getuserlist(){
    
        ArrayList<User> userlist = new ArrayList<User>();
        Connection conn = getconnexion();
        String query = "SELECT nom, prenom, email FROM clients";
        Statement st; // envoie requete 
        ResultSet rs; // contient le resultat de la requete 
        
        try {
          st = conn.createStatement();
          rs = st.executeQuery(query);
          User user;
            while(rs.next()){
                user = new User(rs.getString("nom"),rs.getString("prenom"),rs.getString("email"));
                userlist.add(user);
            }
       
        }catch (Exception e){
             System.out.println(e.getMessage());
        }
        return userlist; // envoie de tous les resultat 
    }
    
