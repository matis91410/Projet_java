    public ArrayList<Produit> getlisteproduit(){
        ArrayList<Produit> listeproduit = new ArrayList<Produit>();
        Connection conn = getconnexion();
        String rqt = "SELECT nom_produit, description, prix, stock FROM produit";
        Statement st;
        ResultSet rs;
        
        try{
          st =  conn.createStatement();
          rs = st.executeQuery(rqt);
          Produit produit;
             while(rs.next()){
                 produit = new Produit(rs.getString("nom_produit"), rs.getString("description"), rs.getInt("prix"), rs.getString("stock"));
                 listeproduit.add(produit);
             }
        
        }catch (Exception e){
             System.out.println(e.getMessage());
        }
        return listeproduit;
    }
    
    public void voirlisteproduit(){
        ArrayList<Produit> list = getlisteproduit();
        DefaultTableModel model = (DefaultTableModel) tableproduit.getModel();
        Object[] row = new Object[4];
        for(int i = 0; i<list.size(); i++){
            row [0] = list.get(i).getnom_produit();
            row [1] = list.get(i).getdescription();
            row [2] = list.get(i).getprix();
            row [3] = list.get(i).getstock();
          model.addRow(row);
        }
    }

