    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        // TODO add your handling code here:
        try{
                
            Connection con = getconnexion();
            
            String produit = edtproduit.getText();
            String description = edtdescription.getText();
            String prix = edtprix.getText();
            String stock = edtstock.getText();
            
            String dr = "SELECT p.nom_produit FROM produit p INNER JOIN detailscommandes dc ON dc.id_produit=p.id_produit";
            Statement ps = con.createStatement();
            ResultSet rs = ps.executeQuery(dr);
                
                if (rs.next()) {
                    

                    String cmd ="SELECT id_commande FROM detailscommandes WHERE id_produit = (SELECT id_produit FROM produit WHERE nom_produit = ?)";
                    PreparedStatement cmdst = con.prepareStatement(cmd);
                    cmdst.setString(1, produit);
                    ResultSet cmdrs = cmdst.executeQuery();


                    String vf ="DELETE FROM detailscommandes WHERE id_produit = (SELECT id_produit FROM produit WHERE nom_produit = ?)";
                    PreparedStatement pc = con.prepareStatement(vf);
                    pc.setString(1, produit);
                    pc.executeUpdate();


                        while (cmdrs.next()) {

                            int id_commande = cmdrs.getInt("id_commande");

                            String gc = "DELETE FROM commande WHERE id_commande = ?";
                            PreparedStatement pscmd = con.prepareStatement(gc);
                            pscmd.setInt(1, id_commande);
                            pscmd.executeUpdate();
                        }
                    String sql =  "DELETE FROM produit  WHERE nom_produit = ?";
                    PreparedStatement pv = con.prepareStatement(sql);
                    pv.setString(1,produit);
                    pv.executeUpdate();
                    
                    JOptionPane.showMessageDialog(this, "Produit Supprimer avec succès !");

                    con.close();
                    pv.close();
                    
                }
                else {

                        String sql =  "DELETE FROM produit  WHERE nom_produit = ?";

                        PreparedStatement pv = con.prepareStatement(sql);

                        pv.setString(1,produit);
                        pv.executeUpdate();


                        JOptionPane.showMessageDialog(this, "Produit Supprimer avec succès !");

                        con.close();
                        pv.close();  
                }
            
            
            
        } 
        catch (SQLException e){
            System.out.println(e.getMessage());
        }
     
    }//GEN-LAST:event_supprimerActionPerformed

