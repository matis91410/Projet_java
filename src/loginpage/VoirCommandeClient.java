    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
                
            if ("En cours".equals(edtetatc.getSelectedItem().toString()))  {
                   try{ 
                        Connection con = getconnexion();

                            String cmd ="SELECT stock FROM produit WHERE id_produit = (SELECT id_produit FROM detailscommandes WHERE id_commande ='"+edtnumero.getText()+"')";
                            PreparedStatement cmdst = con.prepareStatement(cmd);
                            ResultSet cmdrs = cmdst.executeQuery();

                                if (cmdrs.next()){

                                    int stock = cmdrs.getInt("stock");
                                    int quantite = (int) edtquantite.getValue();

                                        if (stock>=quantite){
                                            String sql = "UPDATE detailscommandes SET quantite = '"+edtquantite.getValue()+"' WHERE id_commande = '"+edtnumero.getText()+"'";
                                            PreparedStatement ps = con.prepareStatement(sql);
                                            ps.executeUpdate();
                                            
                                            JOptionPane.showMessageDialog(this, "Commande modifier avec succès!");
                                        }

                                        else {
                                            JOptionPane.showMessageDialog(this, "Commande ne pouvant être modifier car stock insufisant selon la quantité choisit !");
                                        }
                                }

                        con.close();
                        cmdst.close();
                    } 
                    catch (SQLException e){
                        System.out.println(e.getMessage());
                    }
           } else {
              JOptionPane.showMessageDialog(this, "Commande ne peut être modifier!");
           }
            
        
        
    }//GEN-LAST:event_modifierActionPerformed

