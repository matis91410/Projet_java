    public ArrayList<Commandec> getlistecommandec(){
        ArrayList<Commandec> listecommandec = new ArrayList<Commandec>();
        Connection con = getconnexion();
        String rqt = "SELECT c.id_commande,p.nom_produit,c.date_commande,d.quantite,e.nom_etat,c.montant_total FROM commande c INNER JOIN detailscommandes d ON c.id_commande = d.id_commande INNER JOIN produit p ON p.id_produit = d.id_produit INNER JOIN etat e ON e.id_etat = c.id_etat";
        Statement st;
        ResultSet rs;
        try{
            st = con.createStatement();
            rs = st.executeQuery(rqt);
            Commandec commandec;
            while(rs.next()){
                commandec = new Commandec(rs.getInt("id_commande"),rs.getString("nom_produit"), rs.getString("date_commande"), rs.getInt("quantite"), rs.getString("nom_etat"),rs.getInt("montant_total"));
                listecommandec.add(commandec);
            }
        }catch (Exception e ){
                    System.out.println(e.getMessage() );
             }
        return listecommandec;
    }

    public void voirlistecommandec(){
        ArrayList<Commandec> list = getlistecommandec();
        DefaultTableModel model =  (DefaultTableModel) tablecommande.getModel();
        Object[] row = new Object[6];
        for(int i = 0; i<list.size() ; i++){
            
            row [0] = list.get(i).getid_commande();
            row [1] = list.get(i).getnom_produit();
            row [2] = list.get(i).getdate_commande();
            row [3] = list.get(i).getquantite();
            row [4] = list.get(i).getnom_etat();
            row [5] = list.get(i).getmontant_total();
            
            model.addRow(row);
        }
    }
